.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 'Multiplicando:$'
MSG2 DB 10,13, 'Multiplicador:$'
MSG3 DB 10,13, 'Produto:$'

MULTIPLICANDO DB ?
MULTIPLICADOR DB ?
PRODUTO DB ?

.CODE
MAIN PROC

MOV AX,@DATA
MOV DS,AX
XOR CX,CX

;EXIBIR MENSAGEM 1
MOV AH,09
LEA DX,MSG1
INT 21h

;SALVAR O CARACTERE:
MOV AH,01
INT 21h
SUB AL,'0'
MOV MULTIPLICANDO,AL
MOV BL,MULTIPLICANDO


;EXIBIR MENSAGEM 2
MOV AH,09
LEA DX,MSG2
INT 21h

;SALVAR O CARACTERE:
MOV AH,01
INT 21h
SUB AL,'0'
MOV MULTIPLICADOR,AL
MOV CL,MULTIPLICADOR
XOR AX,AX

SOMA:
ADD AX,BX
LOOP SOMA

ADICIONAR:
MOV PRODUTO,AL

FIM:
MOV AH,09
LEA DX,MSG3
INT 21h

MOV AH,02
MOV DL,PRODUTO
ADD DL,'0'
INT 21h

MOV AH,4Ch
INT 21h
MAIN ENDP
END MAIN