.model small
.stack 100h
.DATA
STR1 DB 10 DUP(?)
STR2 DB 10 DUP(?)
QNTD DB 10
RESULT DB 1
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV ES,AX
MOV CX,10
LEA DI,STR1
CALL LERVETOR
XOR CX,CX
MOV CL,QNTD
LEA SI,STR1
CALL IMPRIMIRVETOR
LEA SI,STR1
LEA DI,STR2
CALL MOVEVETOR
LEA SI,STR2
CALL IMPRIMIRVETOR
CALL COMPARA_STRINGS
CALL PROCURAVETOR

MOV AH,4CH
INT 21H
MAIN ENDP 

LERVETOR PROC
PUSH AX
CLD
LENDO:
MOV AH,01
INT 21H
CMP AL,13
JE FIMLV
STOSB
LOOP LENDO
FIMLV:
SUB QNTD,CL
POP AX
RET 
LERVETOR ENDP

IMPRIMIRVETOR PROC
PUSH AX
PUSH DX
CLD
MOV AH,02
MOV DL,10
INT 21H
XOR CX,CX
MOV CL,QNTD
IMPRIMINDO:
LODSB
MOV DL,al
INT 21H
LOOP IMPRIMINDO
POP AX
POP DX
RET 
IMPRIMIRVETOR ENDP

MOVEVETOR PROC
    PUSH ax
    PUSH DX
    mov cl,QNTD
    CLD 
    REP MOVSB 
POP ax
POP DX
RET
MOVEVETOR ENDP

COMPARA_STRINGS PROC
    MOV RESULT, 1            

COMPARA_LOOP:
    LODSB                    
    SCASB                    
    JNE DIFERENTES           
    CMP AL, '$'              
    JE FIM_COMPARACAO       
    JMP COMPARA_LOOP        

DIFERENTES:
    MOV RESULT, 0            

FIM_COMPARACAO:
    RET
COMPARA_STRINGS ENDP
PROCURAVETOR PROC
    CLD 
    LEA DI,STR1 
    MOV AL, 'a'
    SCASB
PROCURAVETOR ENDP
END MAIN