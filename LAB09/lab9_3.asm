.MODEL SMALL
.STACK 100h
.DATA
 VETOR DB 7 DUP(?)
 IMPR DB 10,13,  'DIGITE O VETOR:$'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV AH,09
LEA DX,IMPR
INT 21H
XOR SI,SI
LEA SI, VETOR
CALL LERCARACTERES
XOR SI,SI
MOV DI,6
XOR BX,BX
CALL INVERTER
XOR SI,SI
LEA SI,VETOR
CALL PRINT
MOV AH,4CH
INT 21H
MAIN ENDP



LERCARACTERES PROC

OSSETE:
MOV AH,01
INT 21H
MOV [SI],AL
INC SI
CMP SI,7
JNE OSSETE

RET
LERCARACTERES ENDP

INVERTER PROC

INVERTENDO:
  MOV BL, [SI]
  MOV AL,[DI]
  MOV [DI],BL
  MOV [SI],AL
  DEC DI
  INC SI 
  CMP DI,SI
  JNE INVERTENDO

  RET
INVERTER ENDP

PRINT PROC

IMPRIMIR:
MOV AH,02
MOV DL, [SI]
INT 21H
INC SI
CMP SI,7
JNE IMPRIMIR
RET
PRINT ENDP

END MAIN