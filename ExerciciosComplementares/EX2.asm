
.MODEL SMALL
.STACK 100h
.DATA
vetor db 20 dup(?)
MSG1 DB 'DIGITE SEU NOME:$' 
MSG2 DB 10,13, 'AS INICIAIS SAO: $'
INICAIS DB ?
.CODE
MAIN PROC

MOV AX,@DATA
MOV DS,AX
MOV AH,09
LEA DX,MSG1
INT 21h
XOR BX,BX
XOR SI,SI
XOR CX,CX
MOV CX,20
SALVAR:
MOV AH,1
INT 21h
MOV vetor[SI],AL
INC SI
DEC CX
OCT:
MOV AH,01
INT 21h
DEC CX
CMP AL,' '
JE SALVAR
CMP AL,13
JE LO
CMP CX,0
JNE OCT
LO:
XOR BX,BX
MOV AH,09
LEA DX,MSG2
INT 21h
IMPRIMIR:
MOV AH,02
MOV DL,VETOR[BX]
INT 21H
INC BX
CMP BX,SI
JNE IMPRIMIR

MOV AH,4CH
INT 21H

main ENDP
END MAIN